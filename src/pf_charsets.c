#include "ft_printf.h"

short	is_flag(char c)
{
	return (c == '-' || c == '+' || c == '#' || c == '0' || c == ' ');
}

short	is_length(char c)
{
	return (c == 'h' || c == 'j' || c == 'z' || c == 'l');
}
